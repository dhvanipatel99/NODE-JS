module.exports.add = function(x,y){
    return x + y;
}
module.exports.sub = function(x,y){
    return x - y;
}
module.exports.mul = function(x,y){
    return x * y;
}
module.exports.div = function(x,y){
    return x / y;
}
module.exports.mod = function(x,y){
    return x % y;
}



